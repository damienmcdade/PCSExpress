// Get BAH rate for a given installation and pay grade
export function getBAHRate(installationName, payGrade, withDependents) {
  const mha = INSTALLATION_MHA_MAP[installationName];
  if (!mha) return null;
  const rates = MHA_RATES[mha];
  if (!rates) return null;
  const idx = BAH_PAY_GRADES.indexOf(payGrade);
  if (idx === -1) return null;
  return withDependents ? rates[idx][0] : rates[idx][1];
}

export function getMHAForInstallation(installationName) {
  return INSTALLATION_MHA_MAP[installationName] || null;
}

